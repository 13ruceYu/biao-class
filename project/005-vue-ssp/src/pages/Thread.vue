          但 Apple Music 也不能视为是一个独立的增长点，因为假如不是预装在 iPhone 以及和系统的强整合，Apple Music 恐怕无法在短时间内获得这么快速的增长；Apple News 也一样，能在短时间内成长为美国第一大新闻应用，归根结底我想还是因为大部分用户其实不会很在意自己在哪里看新闻——既然预装了，就用系统预装的呗。
